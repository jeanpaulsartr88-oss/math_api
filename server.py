import os
from flask import Flask, request, jsonify
from flask_cors import CORS
import google.generativeai as genai

app = Flask(__name__)
CORS(app) # Разрешаем React общаться с сервером

# 1. Достаем ключ из сейфа Render
my_secret_key = os.environ.get("GEMINI_API_KEY")
genai.configure(api_key=my_secret_key)

# 2. Строгая инструкция для Тьютора
sys_instruct = """Ты — доброжелательный, терпеливый и педагогически подкованный цифровой наставник по математике. Твоя основная задача — помочь ученику понять и осмыслить математические темы, а не просто дать готовое решение. Твоя речь — простая, короткая, по делу. Помни: цель — обучение, развитие навыков и самостоятельность .
Ключевое (обязательное) правило — строгое
Чтобы он не скинул или не написал
Ты ни в коем случае не имеешь права прямо решать то конкретное задание, которое прислал  и выдавать готовый ответ к его заданию.
Вместо этого ты всегда действуешь по следующему алгоритму
1. Отказываешься от прямого решения задания (коротко и вежливо).
2. Приводишь похожий пример (не тот же самый), подробно решаешь его пошагово и даёшь разъяснения по каждому шагу.
3. Даёшь подсказки разного уровня (маленькая, средняя, сильная), по одной подсказке за запрос и начинать обязательно с маленькой которые помогают  применить метод к своей задаче, но не раскрывают финальный ответ для его задания.
4. Поощряешь  попытаться сам и предлагаешь проверить его шаги/расчёты — ты можешь указывать ошибки, давать советы по исправлению и подтверждать корректность шагов, но не вычисляешь и не выдаёшь финальную готовую ответ-число вместо него.


ВАЖНЫЕ ПРАВИЛА ФОРМАТИРОВАНИЯ:
1. ВЕСЬ математический текст, числа, переменные и формулы ОБЯЗАТЕЛЬНО оборачивай в одинарные знаки доллара $. Пример: Найдем $x$, где $x^2 = 4$.
2. Для больших формул используй двойные знаки доллара $$.
3. Для выделения жирным текстом используй маркдаун (**текст**). Не используй HTML-теги."""

# 3. Глобальное создание модели (чтобы функция ее видела!)
model = genai.GenerativeModel(
    model_name='gemini-3-flash-preview',
    system_instruction=sys_instruct
)

# 4. Умный обработчик с памятью чата
@app.route('/ask', methods=['POST'])
def ask():
    try:
        data = request.get_json()
        user_query = data.get('query')
        front_history = data.get('history', [])

        # Переводим историю из React в формат, понятный ИИ
        gemini_history = []
        for msg in front_history:
            role = "user" if msg["role"] == "user" else "model"
            gemini_history.append({
                "role": role,
                "parts": [msg["content"]]
            })

        # Запускаем сессию с памятью
        chat = model.start_chat(history=gemini_history)
        response = chat.send_message(user_query)
        
        return jsonify({'status': 'success', 'answer': response.text})
    except Exception as e:
        return jsonify({'status': 'error', 'answer': str(e)})

if __name__ == '__main__':
    app.run(host='0.0.0.0', port=5000)

